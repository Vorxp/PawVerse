class Dog {
  final String id;
  final String name;
  final int age;
  final String gender;
  final String size;
  final String description;
  final String ownerName;
  final String imageUrl;

  Dog({
    required this.id,
    required this.name,
    required this.age,
    required this.gender,
    required this.size,
    required this.description,
    required this.ownerName,
    required this.imageUrl,
  });
}
